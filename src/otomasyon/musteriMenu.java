    private void paraCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paraCekActionPerformed
        String bakiye = JOptionPane.showInputDialog(null, "Miktar giriniz", JOptionPane.INFORMATION_MESSAGE);
        if (bakiye != null) {
            for (int i = 0; i < o.musteriler.size(); i++) {
                if (Double.parseDouble(labelTc.getText()) == o.musteriler.get(i).tc) {
                    o.paraGuncelle(o.musteriler.get(i).tc, -Double.parseDouble(bakiye));
                    bakiyeYaz();
                }
            }
        }
    }//GEN-LAST:event_paraCekActionPerformed

    private void paraYatırActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paraYatırActionPerformed
        String bakiye = JOptionPane.showInputDialog(null, "Miktar giriniz", JOptionPane.INFORMATION_MESSAGE);
        if(bakiye!= null){
        for (int i = 0; i < o.musteriler.size(); i++) {
            if (Double.parseDouble(labelTc.getText()) == o.musteriler.get(i).tc) {
                o.paraGuncelle(o.musteriler.get(i).tc, Double.parseDouble(bakiye));
                bakiyeYaz();
                 
            }
        }
        }
    }//GEN-LAST:event_paraYatırActionPerformed

    private void paraTransferiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paraTransferiActionPerformed
        try {
            String tcNo = JOptionPane.showInputDialog(null, "Para transferi yapmak istediğiniz kişinin tc numarasını giriniz", JOptionPane.INFORMATION_MESSAGE);
            String bakiye = JOptionPane.showInputDialog(null, "Miktar giriniz", JOptionPane.INFORMATION_MESSAGE);
            o.paraTransferi(Double.parseDouble(labelTc.getText()), Double.parseDouble(tcNo), Double.parseDouble(bakiye));
            bakiyeYaz();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Girilen degerleri kontrol edip tekrar deneyiniz ", " Uyarı ", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_paraTransferiActionPerformed

