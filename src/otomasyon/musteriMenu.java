    public void bakiyeYaz() {
        for (int i = 0; i < o.musteriler.size(); i++) {
            if (tc == o.musteriler.get(i).tc) {
                System.out.println(o.musteriler.get(i).tc);
                labelBakiye.setText(String.valueOf(o.musteriler.get(i).para));
                labelLimit.setText(String.valueOf(o.musteriler.get(i).limit));
            }
        
        
            
        }
    }

